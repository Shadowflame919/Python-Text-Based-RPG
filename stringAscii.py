def stringToAscii(string, x, y, font, underline=False):
    result = cursorPos(x,y)
    for line in range(len(fonts[font][" "])):
        for letter in string:
            letterLine = fonts[font][letter][line]
            if underline and (line == len(fonts[font][" "])-1):
                for i in range(len(letterLine)):
                    if letterLine[i] == " ":
                        letterLine = letterLine[:i] + "\u2500" + letterLine[i+1:]
            result += letterLine

        result += cursorPos(x,y+line+1)

    return result

def stringToAsciiLength(string, font):
    length = 0
    for letter in string:
        length += len(fonts[font][letter][0])
    return length
    

    
def cursorPos(x,y):
    return "\x1b[" + str(y) + ";" + str(x) + "H"


